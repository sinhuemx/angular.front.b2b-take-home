@Injectable({
  providedIn: 'root'
})
export class OperationsService {
  private ordersSubject = new BehaviorSubject<Order[]>([]);
  private selectedBranches = new Set<number>();
  private startDate: Date | null = null;
  private endDate: Date | null = null;
  private allOrders: Order[] = [];

  constructor() {
    this.loadInitialData();
  }

  loadInitialData() {
    try {
      // Guarda todos los pedidos
      this.allOrders = [...MOCK_ORDERS];
      this.ordersSubject.next(this.allOrders);
    } catch (error) {
      console.error('Error loading orders:', error);
      this.ordersSubject.next([]);
    }
  }

  getOrders(): Observable<Order[]> {
    return this.ordersSubject.asObservable();
  }

  getFilteredOrders(): Observable<Order[]> {
    return this.ordersSubject.pipe(
      map(orders => {
        let filteredOrders = [...orders];

        // Aplicar filtros de sucursal
        if (this.selectedBranches.size > 0) {
          filteredOrders = filteredOrders.filter(order =>
            this.selectedBranches.has(order.branchId)
          );
        }

        // Aplicar filtros de fecha
        if (this.startDate && this.endDate) {
          filteredOrders = filteredOrders.filter(order => {
            const orderDate = new Date(order.updatedAt);
            return orderDate >= this.startDate! && orderDate <= this.endDate!;
          });
        }

        return filteredOrders;
      })
    );
  }

  getAvailableBranches(): Observable<number[]> {
    return this.ordersSubject.pipe(
      map(orders =>
        Array.from(new Set(orders.map(order => order.branchId))).sort((a, b) => a - b)
      )
    );
  }

  setSelectedBranches(branches: Set<number>) {
    this.selectedBranches = branches;
    this.applyFilters();
  }

  setDateRange(startDate: Date | null, endDate: Date | null) {
    this.startDate = startDate;
    this.endDate = endDate;
    if (this.endDate) {
      this.endDate.setHours(23, 59, 59, 999); // Aqui podriamos Ajustar al final del día
    }
    this.applyFilters();
  }

  resetFilters() {
    this.selectedBranches.clear();
    this.startDate = null;
    this.endDate = null;
    this.ordersSubject.next(this.allOrders);
  }

  applyFilters() {
    let filteredOrders = [...this.allOrders];

    if (this.selectedBranches.size > 0) {
      filteredOrders = filteredOrders.filter(order =>
        this.selectedBranches.has(order.branchId)
      );
    }

    if (this.startDate && this.endDate) {
      filteredOrders = filteredOrders.filter(order => {
        const orderDate = new Date(order.updatedAt);
        return orderDate >= this.startDate! && orderDate <= this.endDate!;
      });
    }

    this.ordersSubject.next(filteredOrders);
  }

  calculateStats(orders: Order[]) {
    const totalOrders = orders.length;
    const totalSales = orders.reduce((sum, order) => sum + order.price, 0);
    const averageTicket = totalOrders > 0 ? totalSales / totalOrders : 0;

    return {
      totalOrders,
      totalSales,
      averageTicket
    };
  }
}
